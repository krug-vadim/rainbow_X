fix_t fix_add(fix_t a, fix_t b)
{
	return a + b;
}

fix_t fix_sub(fix_t a, fix_t b)
{
	return a - b;
}

fix_t fix_mod(fix_t a, fix_t b)
{
	if ( b == 0 )
		return 0;
	return a % b;
}

fix_t fix_mul(fix_t a, fix_t b)
{
	return (fix_t)( ((fixd_t)a * (fixd_t)b) >> FIX_FRACT_SIZE );
}

fix_t fix_div(fix_t a, fix_t b)
{
	if ( b == 0 )
		return 0;
	return (fix_t)( (((fixd_t)a) << FIX_FRACT_SIZE) / (fixd_t)b );
}

fix_t fix_sin(fix_t x)
{
	fix_t abs_x;
	fix_t fract;
	fix_t sin_x;

	abs_x = (x >= 0) ? x : -x;
	abs_x &= FIX_ANGLE_MASK;
	fract = FIX_FRACT(abs_x);

	if ( FIX_INT(abs_x)&0x01 )
		fract = 255 - fract;

	sin_x = SIN_TABLE[fract];

	if ( FIX_INT(abs_x)&0x02 )
		sin_x = -sin_x;

	if ( x < 0 )
		sin_x = -sin_x;

	return sin_x;
}

fix_t fix_cos(fix_t x)
{
	return fix_sin(x + FIX_PI_2);
}

#define iter1(N) \
    try = root + (1 << (N)); \
    if (n >= try << (N))   \
    {   n -= try << (N);   \
        root |= 2 << (N); \
    }

fix_t fix_sqrt(fix_t n)
{
	fix_t root = 0, try;

	if ( n <= 0 )
		return 0;
	if ( n == FIX_ONE )
		return FIX_ONE;

	iter1 (15);    iter1 (14);    iter1 (13);    iter1 (12);
	iter1 (11);    iter1 (10);    iter1 ( 9);    iter1 ( 8);
	iter1 ( 7);    iter1 ( 6);    iter1 ( 5);    iter1 ( 4);
	iter1 ( 3);    iter1 ( 2);    iter1 ( 1);    iter1 ( 0);

	return root << ((FIX_FRACT_SIZE/2) - 1);
}

fix_t fix_atan(fix_t x)
{
	if ( x > 0 )
	{
		if ( x == FIX_ONE )
			return FIX_PI_4;
		else
			return (x < FIX_ONE) ?
			       ATAN_TABLE[FIX_FRACT(x)] : FIX_PI_2 - fix_atan(fix_div(FIX_ONE, x));
	}
	else
	{
		x = -x;

		if ( x == FIX_ONE )
			return -FIX_PI_4;
		else
			return (x < FIX_ONE) ?
			       -ATAN_TABLE[FIX_FRACT(x)] : -FIX_PI_2 + fix_atan(fix_div(FIX_ONE, x));
	}
}

fix_t fix_atan2(fix_t x, fix_t y)
{
	fix_t temp;

	temp = fix_div(y, x);

	if ( x == 0 )
	{
		if ( y == 0 )
			return 0;
		else
			return (y > 0) ? FIX_PI_2 : -FIX_PI_2;
	}
	else if ( x > 0 )
		return fix_atan(temp);
	else if ( y >= 0 )
		return fix_atan(temp) + FIX_PI;
	else
		return fix_atan(temp) - FIX_PI;
}
